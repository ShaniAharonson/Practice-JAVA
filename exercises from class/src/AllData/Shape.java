package AllData;

public abstract class Shape {
    public Shape(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    private int x;
private int y;

public void Print(){
    System.out.println("x = "+ getX() + " y= "+ getY());
}
}

